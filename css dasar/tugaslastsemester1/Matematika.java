public class Matematika {
    // Method pertambahan with two integer parameters
    public int pertambahan(int a, int b) {
        return a + b;
    }

    // Method pengurangan with two integer parameters
    public int pengurangan(int a, int b) {
        return a - b;
    }

    // Method perkalian with two integer parameters
    public int perkalian(int a, int b) {
        return a * b;
    }

    // Method pembagian with two double parameters
    public double pembagian(double a, double b) {
        return a / b;
    }
}